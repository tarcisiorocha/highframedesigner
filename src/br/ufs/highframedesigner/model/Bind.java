package br.ufs.highframedesigner.model;

/**
 *
 * @author victorpasqualino
 */
public final class Bind {
    
    private String idCompSource;
    private String idCompDest;
    private String nmIntSource;
    private String nmIntDest;

    public String getIdCompSource() {
        return idCompSource;
    }

    public void setIdCompSource(String idCompSource) {
        this.idCompSource = idCompSource;
    }

    public String getIdCompDest() {
        return idCompDest;
    }

    public void setIdCompDest(String idCompDest) {
        this.idCompDest = idCompDest;
    }

    public String getNmIntSource() {
        return nmIntSource;
    }

    public void setNmIntSource(String nmIntSource) {
        this.nmIntSource = nmIntSource;
    }

    public String getNmIntDest() {
        return nmIntDest;
    }

    public void setNmIntDest(String nmIntDest) {
        this.nmIntDest = nmIntDest;
    }
  
    
}
